public class AhmedSavings {
       public static void main(String[] args) {
double monthlyIncome = 50000;
double monthlyExpenses = 37500;
double laptopCost = 100000;
double monthlySavings = monthlyIncome - monthlyExpenses;
double monthsRequired = laptopCost / monthlySavings;
System.out.println("Monthly Income: Rs. " + monthlyIncome);
System.out.println("Monthly Expenses: Rs. " + monthlyExpenses);
System.out.println("Monthly Savings: Rs. " + monthlySavings);
System.out.println("Laptop Cost: Rs. " + laptopCost);
System.out.println("Months Required to Buy Laptop: " + monthsRequired);
       }
} 
